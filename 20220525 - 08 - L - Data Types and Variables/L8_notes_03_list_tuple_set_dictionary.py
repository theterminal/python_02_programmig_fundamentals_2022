print('\n------- Example 1 ---------------------------- printing the content of a dictionary ------------------\n')
dict_1 = {'name': 'Ivan', 'age': 42, 'smarts': 'very'}
print(dict_1)

print('\n------- Example 2 ---------------------------- printing a \'value\' of a \'key\' from a dictionary -------\n')
print(dict_1['name'])

print('\n------- Example 3 ---------------------------- printing the type of a dictionary ---------------------\n')
print(type(dict_1))

print('\n------- Example 4 ---------------------------- printing the items of a dictionary --------------------\n')
print(dict_1.items())
print(dict_1.items)

print('\n------- Example 5 ---------------------------- printing the keys of a dictionary ---------------------\n')
print(dict_1.keys())
print(dict_1.keys)

print('\n------- Example 6 ---------------------------- printing the values of a dictionary -------------------\n')
print(dict_1.values())
print(dict_1.values)
