# String to List conversion

# list example
lst = ['Garry', 'Eric', 'Bill']

# creating a string 'str_from_list' from the list 'lst' with separators ', '
str_from_list = (', '.join(lst))

# printing the string 'str_from_lst'
print(str_from_list)

# printing the list 'lst' to show that it still exists and it is not converted
print(lst)
