# Note 15 - Adding elements to a dictionary


dict_1 = {
    11: 'eleven',
    12: 'twelve',
    13: 'thirteen'
}
print(dict_1)                   # {11: 'eleven', 12: 'twelve', 13: 'thirteen'}
print(dict_1[11])               # eleven

dict_1[14] = 'fourteen'
print(dict_1)                   # {11: 'eleven', 12: 'twelve', 13: 'thirteen', 14: 'fourteen'}
